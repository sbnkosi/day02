#include <unistd.h>

void ft_putchar(void)
{

char a = '0';
char  b ='1';
char c = '2';

while(a <= '9')
{
	while(b <= '9')
	{
		if(a < b)
		{
			while(c <= '9')
			{
				if(b < c)
				{
					write(1, &a, 1);
					write(1, &b, 1);
					write(1, &c, 1);
					if(a != '7')
					{
					write(1, ", ", 2);
					}
				}
				c++;
			}
			c = '0'; 
		}
		b++;
	}
	b = '0';
	a++;
 }
}
